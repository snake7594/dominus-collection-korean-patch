# 받침 조합: 촉음 ッ + 다음 자음 -> 앞 글자 받침
BATCHIM = {'カ':'ㄱ','キ':'ㄱ','ク':'ㄱ','ケ':'ㄱ','コ':'ㄱ',
           'サ':'ㅅ','シ':'ㅅ','ス':'ㅅ','セ':'ㅅ','ソ':'ㅅ',
           'タ':'ㅅ','チ':'ㅅ','ツ':'ㅅ','テ':'ㅅ','ト':'ㅅ',
           'パ':'ㅂ','ピ':'ㅂ','プ':'ㅂ','ペ':'ㅂ','ポ':'ㅂ'}
JONG = {'ㄱ':1,'ㄴ':4,'ㅅ':19,'ㅂ':17,'ㅇ':21,'ㅁ':16,'ㄹ':8}

def _add_batchim(syl, jong):
    code = ord(syl) - 0xAC00
    if code < 0 or code > 11171 or code % 28 != 0:
        return syl  # 이미 받침 있음/한글 아님
    return chr(ord(syl) + JONG[jong])

def kana_to_hangul(s):
    out = []
    i = 0
    while i < len(s):
        ch = s[i]
        if ch == 'ー':
            i += 1; continue                      # 장음 생략
        if ch == '・':
            out.append('・'); i += 1; continue
        if ch == 'ッ':
            # 다음 자음에 따라 앞 글자에 받침
            nxt = s[i+1] if i+1 < len(s) else ''
            b = BATCHIM.get(nxt, 'ㅅ')
            if out and '가' <= out[-1] <= '힣':
                out[-1] = _add_batchim(out[-1], b)
            i += 1; continue
        if ch == 'ン':
            b = 'ㅇ' if (i+1 < len(s) and s[i+1] in 'カキクケコガギグゲゴ') else 'ㄴ'
            if out and '가' <= out[-1] <= '힣':
                out[-1] = _add_batchim(out[-1], b)
            i += 1; continue
        if i+1 < len(s) and s[i:i+2] in DIGRAPH:
            out.append(DIGRAPH[s[i:i+2]]); i += 2; continue
        if ch in MONO:
            out.append(MONO[ch]); i += 1; continue
        out.append(ch); i += 1
    return ''.join(out)
